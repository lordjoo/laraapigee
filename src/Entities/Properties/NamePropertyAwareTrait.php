<?php

namespace Lordjoo\LaraApigee\Entities\Properties;

trait NamePropertyAwareTrait
{
    /**
     * @var string
     */
    protected string $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return $this
     */
    public function setName(string $name): static
    {
        $this->name = $name;
        return $this;
    }
}
